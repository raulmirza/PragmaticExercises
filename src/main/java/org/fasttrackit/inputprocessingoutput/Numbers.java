package org.fasttrackit.inputprocessingoutput;

public class Numbers {

    public int getSum(int firstNumber, int secondNumber) {

        int sum = firstNumber + secondNumber;
        sum = firstNumber + secondNumber;
        System.out.println(sum);

        return sum;

    }

    public int getDif(int firstNumber, int secondNumber) {
        int dif = secondNumber - firstNumber;
        dif = firstNumber - secondNumber;
        System.out.println(dif);
        return dif;
    }


}